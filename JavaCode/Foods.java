package com.example.healthcaremain;

public class Foods {

    String name;
    String kal;

    public Foods(){

    }

    public Foods(String name, String kal) {
        this.name = name;
        this.kal = kal;
    }

    public String getName() {
        return name;
    }

    public String getKal() {
        return kal;
    }



}
